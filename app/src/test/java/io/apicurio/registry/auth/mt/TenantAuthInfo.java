package io.apicurio.registry.auth.mt;

/**
 * @author Fabian Martinez
 */
public class TenantAuthInfo {

    private String apiClientId;
    private String directAccessClientId;
    private String authServerUrl;
    private String realm;

    private String username;
    private String password;


    public String getRealm() {
        return realm;
    }

    public void setRealm(String realm) {
        this.realm = realm;
    }

    public String getApiClientId() {
        return apiClientId;
    }

    public void setApiClientId(String apiClientId) {
        this.apiClientId = apiClientId;
    }

    public String getDirectAccessClientId() {
        return directAccessClientId;
    }

    public void setDirectAccessClientId(String directAccessClientId) {
        this.directAccessClientId = directAccessClientId;
    }

    public String getAuthServerUrl() {
        return authServerUrl;
    }

    public void setAuthServerUrl(String authServerUrl) {
        this.authServerUrl = authServerUrl;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

}
